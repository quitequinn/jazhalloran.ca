				<p>So I am still exploring the creative power and possibilities of the book. In that process, I became an author. It was the editors at the independent publishing house Zone who first declared me one by including me as a contributor to our first Zone Book. They explained that form is content in the same way that content is form. That singular idea set the trajectory for thirty years of work. Understanding that the way we do things, the way things appear, their material character, the way an object unfolds and lives in our minds and our personal ecology, is a story, and that story is partially, and sometimes principally, written by the designer. My obsession with that story has been one of the great loves of my life.</p>
