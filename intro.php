				<p>Ever since I designed my first books in 1985, I put three copies of every book I worked on into my personal archive for my children. What you see here is a selection from my oldest daughter’s collection. It captures my love for the book as a creative form. In the 1980s there was a lot of nonsense about how the book was dead. I didn’t see it that way. The book is not an outmoded graphic, two-dimensional form. I think of the book as a dynamic, cinematic, four-dimensional object evolving in the hands and mind of the reader. As a creator, my work is half the work. The reader takes the work to their own conclusion.The book is one of the most intimate cultural objects we have. We curl up with it, we take it to bed, we cry quietly with it at times. We can also have an intensely private experience in public while reading our book on a streetcar or subway. No, the book is not dead. It is just getting started. Most importantly, the book still has the aura of authority.</p>
